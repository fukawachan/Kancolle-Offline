import { writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { createFrozenSourceSession, generatedOutputPath } from "./lib/frozen-source.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");
const OUTPUT = generatedOutputPath(path.join(ROOT, "src", "master", "shop-data.ts"));
const KANCOLLE_DATA_COMMIT = "a8018819ad330b73c714fbba195794453c8dfde3";
const START2_URL = `https://raw.githubusercontent.com/kcwiki/kancolle-data/${KANCOLLE_DATA_COMMIT}/api/api_start2.json`;
const EXPECTED_PAYITEM_COUNT = 32;
const session = await createFrozenSourceSession("shop", {
  generator: "scripts/generate-shop-data.mjs",
  userAgent: "kancolle-local-shop-generator"
});

function num(value, label) {
  const normalized = Number(value);
  if (!Number.isFinite(normalized)) throw new Error(`Invalid ${label}: ${value}`);
  return normalized;
}

function normalizePayItem(item) {
  if (!item || typeof item !== "object") throw new Error("Invalid payitem row");
  if (!Array.isArray(item.api_item) || item.api_item.length !== 8) {
    throw new Error(`Payitem ${item.api_id} has invalid api_item`);
  }
  return {
    api_id: num(item.api_id, "api_id"),
    api_type: num(item.api_type ?? 0, "api_type"),
    api_name: String(item.api_name ?? ""),
    api_description: String(item.api_description ?? ""),
    api_shop_description: String(item.api_shop_description ?? item.api_description ?? ""),
    api_item: item.api_item.map((value, index) => num(value, `api_item[${index}]`)),
    api_price: num(item.api_price ?? 0, "api_price")
  };
}

function normalizeCabinet(raw, label) {
  if (!Array.isArray(raw)) throw new Error(`Missing ${label}`);
  return raw.map((value) => Math.trunc(num(value, label)));
}

function validatePayItems(payItems) {
  if (payItems.length !== EXPECTED_PAYITEM_COUNT) {
    throw new Error(`Expected ${EXPECTED_PAYITEM_COUNT} payitems, got ${payItems.length}`);
  }
  const ids = new Set();
  for (const item of payItems) {
    if (ids.has(item.api_id)) throw new Error(`Duplicate payitem ${item.api_id}`);
    if (!item.api_name) throw new Error(`Payitem ${item.api_id} is missing api_name`);
    ids.add(item.api_id);
  }
  return ids;
}

function normalizeItemShop(raw, payItemIds) {
  if (!raw || typeof raw !== "object") throw new Error("Missing api_mst_item_shop");
  const shop = {
    api_cabinet_1: normalizeCabinet(raw.api_cabinet_1, "api_cabinet_1"),
    api_cabinet_2: normalizeCabinet(raw.api_cabinet_2, "api_cabinet_2")
  };
  for (const id of [...shop.api_cabinet_1, ...shop.api_cabinet_2]) {
    if (id === -1) continue;
    if (!payItemIds.has(id)) throw new Error(`Shop cabinet references unknown payitem ${id}`);
  }
  return shop;
}

function render(payItems, itemShop) {
  return `// Generated by scripts/generate-shop-data.mjs.
// Sources checked on ${session.generatedAt.slice(0, 10)}:
// - ${START2_URL}
// Do not edit this file by hand.

export type PayItemMaster = {
  api_id: number;
  api_type: number;
  api_name: string;
  api_description: string;
  api_shop_description: string;
  api_item: number[];
  api_price: number;
};

export type ItemShopMaster = {
  api_cabinet_1: number[];
  api_cabinet_2: number[];
};

export const SHOP_PAYITEM_MASTERS: PayItemMaster[] = ${JSON.stringify(payItems, null, 2)};

export const SHOP_ITEM_SHOP: ItemShopMaster = ${JSON.stringify(itemShop, null, 2)};
`;
}

const rawStart2 = await session.readJson("start2.json", START2_URL, {
  revision: KANCOLLE_DATA_COMMIT,
  evidence: "exact",
  license: {
    spdx: "NOASSERTION",
    url: `https://github.com/kcwiki/kancolle-data/tree/${KANCOLLE_DATA_COMMIT}`,
    note: "Community master-data snapshot; consult the pinned upstream repository for terms"
  }
});
const start2 = rawStart2.api_data ?? rawStart2;
const payItems = (start2.api_mst_payitem ?? []).map(normalizePayItem);
const payItemIds = validatePayItems(payItems);
const itemShop = normalizeItemShop(start2.api_mst_item_shop, payItemIds);

await writeFile(OUTPUT, render(payItems, itemShop));
await session.finalize({ repositoryRevision: KANCOLLE_DATA_COMMIT });
console.log(`Generated ${payItems.length} shop payitems at ${OUTPUT}`);
